<!-- Faça um script que peça a temperatura em graus Fahrenheit, transforme emostre a temperatura em graus Celsius. C = (5 * (F-32) / 9). -->

<?php 
// adicionando dados as variáveis 
$Fahrenheit = 10;

// realizando o calculo da conversão
$conver = (5*($Fahrenheit-32)/9);

// mostrando em tela 
echo "o valor equivalente em graus Celcius é de: " .$conver. " graus Celcius.";

?>